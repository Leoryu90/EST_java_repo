package weeklyQuiz;

public class MyCalculator {
    public static int mySum(int a, int b) {
        return a + b;
    }

    public static int myMinus(int a, int b) {
        return a - b;
    }

    public static int myMultiply(int a, int b) {
        return a * b;
    }

    public static int myDivide(int a, int b) {
        return a / b;
    }
}
