package EST_java.day4;

public class Java_20240603_1 {
    public static void main(String[] args){
        int num1 = 10;
        int num2 = 3;

        int sum = num1 + num2;
        int diff1 = num1 - num2;
        int diff2 = num2 - num1;

        int product = num1 * num2;

        int quotient = num1 / num2;

        int remainder = num1 % num2;

    System.out.println("덧샘 = " + sum);
    System.out.println("뺄샘 = " + diff1);
    System.out.println("뺄샘 = " + diff2);
    System.out.println("곱하기 = " + product);
    System.out.println("나누기 = " + quotient);
    System.out.println("나머지 = " + remainder);
  }
}
