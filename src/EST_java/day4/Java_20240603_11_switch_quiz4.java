package EST_java.day4;

public class Java_20240603_11_switch_quiz4 {
  public static void main(String[] args) {

    String color = "red";

    switch (color) {
      case "red":
        System.out.println("볼");
        break;
      case "blue":
        System.out.println("바다");
        break;
      case "green":
        System.out.println("아보카도");
        break;
    }
  }
}
