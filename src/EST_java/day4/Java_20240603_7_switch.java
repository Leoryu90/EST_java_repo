package EST_java.day4;

public class Java_20240603_7_switch {
  public static void main(String[] args) {

    int data = 10;

    switch (data) {
      case 1:
        System.out.println("1 입니다.");
        break;
      case 2:
        System.out.println("2 입니다.");
        break;
      case 10:
        System.out.println("10");
        break;
      default:
        System.out.println("noting");
    }
  }
}
