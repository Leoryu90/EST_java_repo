package EST_java.day4;

public class Java_20240603_13_switch_quiz6 {
  public static void main(String[] args) {
    String lan = "Java";

    switch (lan) {
      case "Java":
        System.out.println("오르미 5기의 쏘울");
        break;
      case "Python":
        System.out.println("좋은 언어");
        break;
      case "C++":
        System.out.println("좋은 언어 C++");
        break;
      case "JavaScript":
        System.out.println("좋은 프론트엔드 언어");
        break;
    }
  }
}
